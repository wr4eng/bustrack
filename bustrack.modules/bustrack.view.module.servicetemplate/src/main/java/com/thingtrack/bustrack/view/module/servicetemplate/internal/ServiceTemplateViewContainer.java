package com.thingtrack.bustrack.view.module.servicetemplate.internal;

import javax.annotation.PostConstruct;
import javax.annotation.PreDestroy;

import org.springframework.beans.factory.annotation.Autowired;

import com.vaadin.annotations.AutoGenerated;
import com.vaadin.ui.VerticalLayout;

import com.thingtrack.konekti.service.api.ServiceService;
import com.thingtrack.konekti.service.api.ServiceStatusService;
import com.thingtrack.konekti.service.api.ServiceTypeService;
import com.thingtrack.konekti.view.addon.ui.AbstractViewContainer;
import com.thingtrack.konekti.view.addon.ui.SliderView;
import com.thingtrack.konekti.view.kernel.IWorkbenchContext;
import com.thingtrack.konekti.view.kernel.ui.layout.ISliderView;
import com.thingtrack.konekti.view.kernel.ui.layout.IViewChangeListener;
import com.thingtrack.konekti.view.kernel.ui.layout.IView;

import com.thingtrack.bustrack.service.api.RouteStatusService;
import com.thingtrack.bustrack.service.api.ServiceTemplateService;

@SuppressWarnings("serial")
public class ServiceTemplateViewContainer extends AbstractViewContainer {

	@AutoGenerated
	private VerticalLayout mainLayout;
	@AutoGenerated
	private SliderView sliderView;

	/** Views **/
	private ServiceTemplateView serviceTemplateView;
	
	/** Business services **/
	@Autowired
	private ServiceService serviceService;
	@Autowired
	private ServiceTypeService serviceTypeService;
	@Autowired
	private ServiceStatusService serviceStatusService;
	@Autowired
	private RouteStatusService routeStatusService;
	
	@Autowired
	private ServiceTemplateService serviceTemplateService;
	
	private IWorkbenchContext context; 
	
	// Thread Local Bundle Business Services
	private static ThreadLocal<ServiceService> threadServiceService = new ThreadLocal<ServiceService>();
	private static ThreadLocal<ServiceTypeService> threadServiceTypeService = new ThreadLocal<ServiceTypeService>();
	private static ThreadLocal<ServiceStatusService> threadServiceStatusService = new ThreadLocal<ServiceStatusService>();
	private static ThreadLocal<RouteStatusService> threadRouteStatusService = new ThreadLocal<RouteStatusService>();
	private static ThreadLocal<ServiceTemplateService> threadServiceTemplateService = new ThreadLocal<ServiceTemplateService>();
	
	private static ThreadLocal<IWorkbenchContext> threadContext = new ThreadLocal<IWorkbenchContext>();
	
	/*- VaadinEditorProperties={"grid":"RegularGrid,20","showGrid":true,"snapToGrid":true,"snapToObject":true,"movingGuides":false,"snappingDistance":10} */
		
	/**
	 * The constructor should first build the main layout, set the
	 * composition root and then do any custom initialization.
	 *
	 * The constructor will not be automatically regenerated by the
	 * visual editor.
	 */
	public ServiceTemplateViewContainer(IWorkbenchContext context) {
		buildMainLayout();
		setCompositionRoot(mainLayout);

		// TODO add user code here
		this.context = context;
	}

	@SuppressWarnings("unused")
	@PostConstruct
	private void createViews() {
		// initialize thread local bundle services
		threadServiceService.set(serviceService);
		threadServiceTypeService.set(serviceTypeService);
		threadServiceStatusService.set(serviceStatusService);
		threadServiceTemplateService.set(serviceTemplateService);
		threadRouteStatusService.set(routeStatusService);
		
		threadContext.set(context);
				
		// add all views controlled by SliderView Component
		serviceTemplateView = new ServiceTemplateView(context, this);
		sliderView.addView(serviceTemplateView);
		views.put(0, serviceTemplateView);
		
	}

	@SuppressWarnings("unused")
	@PreDestroy
	private void destroyViews() {
		// destroy thread local bundle services
		threadServiceService.set(null);
		threadServiceTypeService.set(null);
		threadServiceStatusService.set(null);
		threadServiceTemplateService.set(null);
		threadRouteStatusService.set(null);
		
		threadContext.set(null);
		
	}
	
    public static ServiceService getServiceService() {
        return threadServiceService.get();
        
    }
    
    public static ServiceTypeService getServiceTypeService() {
        return threadServiceTypeService.get();
        
    }
    
    public static ServiceStatusService getServiceStatusService() {
        return threadServiceStatusService.get();
        
    }
    
    public static ServiceTemplateService getServiceTemplateService() {
        return threadServiceTemplateService.get();
        
    }

    public static RouteStatusService getServiceRouteStatusService() {
        return threadRouteStatusService.get();
        
    }
    
    public static IWorkbenchContext getContext() {
        return threadContext.get();
        
    }
    
	@Override
	public ISliderView getSliderView() {
		return sliderView;
		
	}
	
	@Override
	public IView getSelectedView() {
		return sliderView.getSelectedView();
		
	}
	
	@Override
	public void addListener(IViewChangeListener listener) {
		sliderView.addListener(listener);
		
	}
	
	@AutoGenerated
	private VerticalLayout buildMainLayout() {
		// common part: create layout
		mainLayout = new VerticalLayout();
		mainLayout.setImmediate(false);
		mainLayout.setWidth("100%");
		mainLayout.setHeight("100%");
		mainLayout.setMargin(false);
		
		// top-level component properties
		setWidth("100.0%");
		setHeight("100.0%");
		
		// sliderView_1
		sliderView = new SliderView();
		sliderView.setImmediate(false);
		sliderView.setWidth("100.0%");
		sliderView.setHeight("100.0%");
		mainLayout.addComponent(sliderView);
		mainLayout.setExpandRatio(sliderView, 1.0f);
		
		return mainLayout;
	}

}
