	@Override
	public void deleteButtonClick(EditionToolbar.ClickNavigationEvent event) {
		final ServiceTemplate editingServiceTemplate = (ServiceTemplate) event.getRegister();
		
		if (editingServiceTemplate == null)
			return;
		
		ConfirmDialog.show(getWindow(), "Borrar Plantilla de Servicios", "¿Estás seguro?", "Si", "No",
		        new ConfirmDialog.Listener() {

		            public void onClose(ConfirmDialog dialog) {
		                if (dialog.isConfirmed()) {
		            		try {
		            			serviceTemplateService.delete(editingServiceTemplate);
		            			
		            			bsServiceTemplate.removeItem(editingServiceTemplate);		            			
		            		} catch (IllegalArgumentException e) {
		            			throw new RuntimeException("¡No se pudo borrar la plantilla de servicios!", e);
		            		} catch (Exception e) {
		            			throw new RuntimeException("¡No se pudo borrar el plantilla de servicios!", e);
		            		}
		                } 
		            }
		        });
		
	}

	@Override
	public void filterButtonClick(BoxToolbar.ClickNavigationEvent event) {
		if (masterDetailLayout.getActiveCard().equals(MasterDetailLayout.TOP_CARD))
			dgServiceTemplate.setFilterBarVisible();
		else if (masterDetailLayout.getActiveCard().equals(MasterDetailLayout.BOTTON_CARD))
			dgService.setFilterBarVisible();
	}

	@Override
	public void printButtonClick(BoxToolbar.ClickNavigationEvent event) {
		try {
			if (masterDetailLayout.getActiveCard().equals(MasterDetailLayout.TOP_CARD))
				dgServiceTemplate.print("Maestro Plantilla de Servicios");
			else if (masterDetailLayout.getActiveCard().equals(MasterDetailLayout.BOTTON_CARD)) {
				ServiceTemplate serviceTemplate = bsServiceTemplate.getItemId();
				
				if (serviceTemplate != null)					
					dgService.print("Maestro de Servicios de la plantilla " + serviceTemplate.getDescription());
			}
		}
		catch (Exception e) {
			throw new RuntimeException("¡No se pudo imprimir el informe!", e);
		}
		
	}
	
	@Override
	public void cardClick(ClickCardEvent event) {			
		if (event.getPosition().equals(MasterDetailLayout.TOP_CARD)) {
			if (event.getPosition() != masterDetailLayout.getActiveCard()) {
				injectServiceTemplateBindingSource();
			
				ServiceTemplate serviceTemplate = (ServiceTemplate)bsServiceTemplate.getItemId();
				
				if (serviceTemplate == null)
					return;
									
				refreshServiceBindindSource(serviceTemplate);
			}
		
		}
		else if (event.getPosition().equals(MasterDetailLayout.BOTTON_CARD)) {							
			if (event.getPosition() != masterDetailLayout.getActiveCard())
				injectServiceBindingSource();
			
		}
		
	}	

	@Override
	public void bindingSourceIndexChange(IndexChangeEvent event) {
		if (masterDetailLayout.getActiveCard().equals(MasterDetailLayout.TOP_CARD)) {
			if (event.getRegister().getClass().equals(ServiceTemplate.class)) {
				ServiceTemplate serviceTemplate = (ServiceTemplate)event.getRegister();
				
				if (serviceTemplate == null)
					return;
									
				refreshServiceBindindSource(serviceTemplate);
			}
		}
				
	}
	
	@Override
	public void parsingTemplateButtonClick(ServiceTemplateToolbar.ClickNavigationEvent event) {
		final ServiceTemplate editingServiceTemplate = (ServiceTemplate) event.getRegister();
		
		List<Service> services = null;
		try {
			services = serviceTemplateParser.parse(editingServiceTemplate);
			
		} catch (FileNotFoundException e) {
			throw new RuntimeException("¡No se puede parsear el fichero!", e);
		} catch (IOException e) {
			throw new RuntimeException("¡No se puede parsear el fichero!", e);
		} catch (ServiceParsingException e) {
			throw new RuntimeException("¡No se puede parsear el fichero!", e);
		} catch (NoServiceException e) {
			throw new RuntimeException("¡No se puede parsear el fichero!", e);
		} catch (Exception e) {
			throw new RuntimeException("¡No se puede parsear el fichero!", e);
		}
		
		if (services.size() == 0)
			return;
		
		try {
			serviceTemplateService.saveAllServiceTemplate(services);
			
		} catch (Exception e) {
			throw new RuntimeException("¡No se puede parsear el fichero!", e);
		}
		
	}
	
